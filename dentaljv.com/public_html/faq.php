          <h4 class="trigger"><a href="#q1">¿Qué es un implante dental?</a></h4>
          <div id="q1" class="toggle_container">
            <p>Una raíz artificial que funciona como la raíz del diente natural que se ha perdido.</p>
            <p>Cuando faltan un diente y su raíz, la solución a largo plazo para que se mantenga la función natural es un implante dental y una corona cuya apariencia coincida con la del diente original.</p>
          </div>
          <h4 class="trigger"><a href="#q2">¿Cuál es la principal ventaja del tratamiento con implantes dentales?</a></h4>
          <div id="q2" class="toggle_container">
            <p>Gracias a los implantes dentales podemos conservar el hueso maxilar y mantenerlo en plena forma, evitando el deterioro que se produce cuando se pierde uno o más dientes debido al cese en las fuerzas masticatorias.</p>
            <p>Los implantes dentales pueden contribuir a evitar el deterioro óseo gradual puesto que reproducen la acción de la raíz original, transmitiendo fuerzas masticatorias al hueso maxilar de una forma natural. Algo que no sucede con métodos convencionales como puentes o prótesis parciales.</p>
          </div>
          <h4 class="trigger"><a href="#q3">¿Es el tratamiento con implantes una solución costosa?</a></h4>
          <div id="q3" class="toggle_container">
            <p>Al contrario de lo que se podría pensar, las prótesis convencionales no siempre son una solución económica.</p>
            <p>El diseño inicial para el tratamiento con implantes dentales varía en función del número de implantes necesarios y si se necesita aumento de hueso. Otros factores incluyen la propia cirugía, el trabajo del laboratorio, la rehabilitación protética, la complejidad del caso, etc.</p>
            <p>Solo puede elaborarse un presupuesto una vez que se han abordado todos estos aspectos y, en algunas situaciones, los implantes dentales pueden ser más rentables a largo plazo.</p>
          </div>
          <h4 class="trigger"><a href="#q4">¿Existen riesgos en la colocación de los implantes?</a></h4>
          <div id="q4" class="toggle_container">
            <p>El tratamiento con implantes no es un enfoque experimental, sino más bien una técnica dental avanzada y ampliamente acreditada que ha sido probada científicamente y utilizada durante décadas por odontólogos y cirujanos de todo el mundo.</p>
            <p>Dependiendo del estado de salud de cada paciente, los factores potenciales de riesgo pueden variar. Una higiene bucal deficiente o una cicatrización limitada de la herida a causa de enfermedades preexistentes pueden reducir las probabilidades de que el implante se conserve.</p>
            <p>Puesto que todos los procedimientos quirúrgicos implican riesgos potenciales, su odontólogo evaluará si los implantes dentales son la solución para usted.</p>
          </div>
          <h4 class="trigger"><a href="#q5">¿Es una cirugía dolorosa?</a></h4>
          <div id="q5" class="toggle_container">
            <p>Los implantes dentales se insertan generalmente con anestesia local para reducir las molestias Si tiene cualquier duda sobre el estado de la zona del implante después de la intervención, asegúrese de consultárselo al dentista.</p>

          </div>

          <h4 class="trigger"><a href="#q6">¿Existen cifras sobre el nivel de satisfacción de las personas con implantes?</a></h4>
          <div id="q6" class="toggle_container">
            <p>De acuerdo con una encuesta efectuada a más de 10 000 pacientes de Alemania (encuesta Riegl 6), el 66 % de los encuestados se mostraron «muy satisfechos» con sus restauraciones implantosoportadas y el 31 % declaró estar «satisfechos». De los pacientes con restauraciones convencionales sin implantes, solo el 42 % declaraba estar «muy satisfechos» y un 44 % declaró que estaban satisfechos en general.</p>

          </div>

          <h4 class="trigger"><a href="#q7">¿Hay alguna limitación al tratamiento con implantes?</a></h4>
          <div id="q7" class="toggle_container">
            <p>Los implantes pueden insertarse en cuanto la mandíbula haya dejado de crecer. Por ello, en los pacientes jóvenes, los implantes dentales solo se pueden colocar una vez que los huesos maxilares estén totalmente desarrollados. En el otro extremo del espectro no hay ningún límite de edad, siempre y cuando, por supuesto, el paciente no presente factores de riesgo médicos considerables.</p>

